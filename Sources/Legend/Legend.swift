import Foundation

/// Enumeration representing the display type of the legend
public enum Legend {
    /// Show legend in square form
    /// 以正方形形式显示图例
    case square
    /// Show legend in triangle form
    /// 以三角形形式显示图例
    case triangle
    /// Show legend in circle form
    /// 以圆形形式显示图例
    case circle
    /// Show legend in pentagram form
    /// 以五角星形式显示图例
    case pentagram
    /// Show legend in hexagon form
    /// 以六边形形式显示图例
    case hexagon
}
